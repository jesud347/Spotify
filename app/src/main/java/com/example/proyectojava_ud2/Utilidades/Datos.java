    public static ArrayList<Cancion> getListaCanciones(String tituloAlbum) {
        ArrayList<Cancion> lista = todasLasCanciones();
        ArrayList<Cancion> cancionesAlbum = new ArrayList<>();
        for (Cancion c: lista) {
            if(c.getAlbum().getTitulo().equals(tituloAlbum)){
                cancionesAlbum.add(c);
            }
        }
        return cancionesAlbum;
    }
